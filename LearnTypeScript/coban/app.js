/// Kiểu String
var message = "Hello, TypeScript!";
console.log("Message: " + message);
/// Kiểu number: Số nguyên và số thực
var num = 2;
console.log("Num: " + num);
/// Kiểu boolean: giá trị đúng sai
var bool = true;
console.log("Boolean: " + bool);
/// Kiểu null: kiểu không có giá trị
var n = null;
console.log("Null: " + n);
/// Kiểu undefined: kiểu không xác định
var u = undefined;
console.log("Undefined: " + u);
